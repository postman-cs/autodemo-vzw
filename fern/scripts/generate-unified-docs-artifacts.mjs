import fs from "node:fs/promises";
import path from "node:path";
import { execFileSync } from "node:child_process";

const SCRIPT_DIR = path.dirname(new URL(import.meta.url).pathname);
const FERN_ROOT = path.resolve(SCRIPT_DIR, "..");
const REPO_ROOT = path.resolve(FERN_ROOT, "..");
const INTERNAL_CATALOG_ROOT = path.join(REPO_ROOT, "internal-catalog");
const DOCS_PATH = path.join(FERN_ROOT, "docs.yml");
const APIS_DIR = path.join(FERN_ROOT, "apis");
const LANDING_PATH = path.join(FERN_ROOT, "pages", "landing.mdx");
const SERVICE_PAGES_DIR = path.join(FERN_ROOT, "pages", "services");
const REGISTRY_PATH = path.join(INTERNAL_CATALOG_ROOT, "specs", "registry.json");
const MANIFEST_FIXTURE_PATH = path.join(INTERNAL_CATALOG_ROOT, "test", "fixtures", "canonical-manifest.json");

const TAB_METADATA = {
  "emergency-dispatch": {
    docsLabel: "Emergency Dispatch",
    sectionTitle: "Emergency Dispatch Operations",
    icon: "light fa-truck-medical",
  },
  "private-5-g-campus": {
    docsLabel: "Private 5G Campus",
    sectionTitle: "Private 5G Campus Operations",
    icon: "light fa-tower-cell",
  },
  "utility-grid": {
    docsLabel: "Utility Grid",
    sectionTitle: "Utility Grid Intelligence",
    icon: "light fa-bolt",
  },
  "platform-services": {
    docsLabel: "Platform Services",
    sectionTitle: "Platform Services",
    icon: "light fa-gear",
  },
};

function escapeMdx(value) {
  return String(value ?? "").replace(/\\/g, "\\\\").replace(/\"/g, '\\\"');
}

function compactDescription(description) {
  if (!description) return "Live API reference and service operations metadata.";
  const oneLine = String(description).replace(/\s+/g, " ").trim();
  if (oneLine.length <= 140) return oneLine;
  return `${oneLine.slice(0, 137).trimEnd()}...`;
}

function getPathname(url) {
  try {
    return new URL(url).pathname;
  } catch {
    return "#";
  }
}

function trimApiDisplayTitle(title) {
  return String(title ?? "")
    .replace(/^VZW\s+/, "")
    .replace(/\s+API$/, "")
    .trim();
}

function toPosixPath(filePath) {
  return filePath.split(path.sep).join(path.posix.sep);
}

async function readJson(filePath) {
  return JSON.parse(await fs.readFile(filePath, "utf8"));
}

function buildManifestEvalScript() {
  return [
    'import { buildCanonicalManifest } from "./src/lib/docs-manifest.ts";',
    'import { listResolvedDeployments } from "./src/lib/deployment-state.ts";',
    '(async () => {',
    '  const env = process.env;',
    '  const hasAirtable = Boolean(env.AIRTABLE_API_KEY && env.AIRTABLE_BASE_ID);',
    '  const ghToken = typeof env.GH_TOKEN === "string" ? env.GH_TOKEN.trim() : "";',
    '  const deployments = hasAirtable ? await listResolvedDeployments(env, ghToken) : [];',
    '  process.stdout.write(JSON.stringify(buildCanonicalManifest(deployments)));',
    '})();',
  ].join("\n");
}

function loadManifestViaTsx() {
  const output = execFileSync(
    "npx",
    ["--prefix", INTERNAL_CATALOG_ROOT, "tsx", "--eval", buildManifestEvalScript()],
    {
      cwd: INTERNAL_CATALOG_ROOT,
      encoding: "utf8",
      env: process.env,
      stdio: ["ignore", "pipe", "pipe"],
    },
  );

  return JSON.parse(output);
}

async function loadManifest() {
  if (process.argv.includes("--fixture-only")) {
    return readJson(MANIFEST_FIXTURE_PATH);
  }

  try {
    return loadManifestViaTsx();
  } catch (error) {
    const message = error instanceof Error ? error.message : String(error);
    console.warn(`Falling back to canonical manifest fixture: ${message}`);
    return readJson(MANIFEST_FIXTURE_PATH);
  }
}

function buildDocsYaml(manifest) {
  const lines = [
    "# Generated by fern/scripts/generate-unified-docs-artifacts.mjs. Do not edit by hand.",
    "instances:",
    "  - url: vzw-demo.docs.buildwithfern.com",
    "",
    "title: Verizon Partner API Documentation",
    "favicon: ./assets/favicon.svg",
    "",
    "logo:",
    "  href: https://partner.vzw.pm-demo.dev",
    "  light: ./assets/verizon-logo-light.svg",
    "  dark: ./assets/verizon-logo-dark.svg",
    "  height: 28",
    "",
    "colors:",
    "  accent-primary:",
    "    light: \"#EE0000\"",
    "    dark: \"#FF3333\"",
    "  background:",
    "    light: \"#FFFFFF\"",
    "    dark: \"#111111\"",
    "  sidebar-background:",
    "    light: \"#FAFAFA\"",
    "    dark: \"#1A1A1A\"",
    "  header-background:",
    "    light: \"#FFFFFF\"",
    "    dark: \"#111111\"",
    "",
    "navbar-links:",
    "  - type: minimal",
    "    text: Partner Portal",
    "    href: https://partner.vzw.pm-demo.dev",
    "  - type: filled",
    "    text: Run in Postman",
    "    href: https://postman.com/intercept/run",
    "    rounded: true",
    "",
    "layout:",
    "  searchbar-placement: header",
    "  tabs-placement: header",
    "",
    "css: ./styles.css",
    "",
    "js:",
    "  - path: ./custom.js",
    "    strategy: afterInteractive",
    "theme:",
    "  sidebar: minimal",
    "tabs:",
  ];

  for (const tab of manifest.tabs) {
    const metadata = TAB_METADATA[tab.slug];
    if (!metadata) {
      throw new Error(`Unsupported manifest tab slug: ${tab.slug}`);
    }
    lines.push(`  ${tab.slug}:`);
    lines.push(`    display-name: ${metadata.docsLabel}`);
    lines.push(`    icon: ${metadata.icon}`);
  }

  lines.push("", "navigation:");
  for (const tab of manifest.tabs) {
    lines.push(`  - tab: ${tab.slug}`);
    lines.push("    layout:");
    for (const service of tab.services) {
      lines.push(`      - api: ${trimApiDisplayTitle(service.title)}`);
      lines.push(`        api-name: ${service.id}`);
    }
  }

  return `${lines.join("\n")}\n`;
}

function buildGeneratorYaml(service) {
  const generatorFile = path.join(APIS_DIR, service.id, "generators.yml");
  const manifestSpecPath = path.join(INTERNAL_CATALOG_ROOT, "specs", service.sourceSpec);
  const relativeSpecPath = toPosixPath(path.relative(path.dirname(generatorFile), manifestSpecPath));

  return [
    "# Generated by fern/scripts/generate-unified-docs-artifacts.mjs. Do not edit by hand.",
    "api:",
    "  specs:",
    `    - openapi: ${relativeSpecPath}`,
    "",
  ].join("\n");
}

function buildServicePage(service, tab) {
  const graphName = TAB_METADATA[tab.slug]?.sectionTitle ?? tab.title;
  const lines = [
    "---",
    `title: ${service.title}`,
    "---",
    "",
    'import { ServiceOverview } from "@/components/ServiceOverview"',
    "",
    "<ServiceOverview",
    `  serviceId=\"${escapeMdx(service.id)}\"`,
    `  title=\"${escapeMdx(service.title)}\"`,
    `  description=\"${escapeMdx(service.description)}\"`,
    `  graphName=\"${escapeMdx(graphName)}\"`,
    `  runtime=\"${escapeMdx(service.runtime)}\"`,
    "/>",
    "",
  ];

  return lines.join("\n");
}

function buildLandingPage(manifest) {
  const lines = [
    "<!-- Generated by fern/scripts/generate-unified-docs-artifacts.mjs. Do not edit by hand. -->",
    "---",
    "title: Verizon Partner API Catalog",
    "---",
    "",
    `Explore ${manifest.serviceCount} enterprise APIs across emergency dispatch, private 5G campus networks, utility grid intelligence, and platform services.`,
    "",
  ];

  for (const tab of manifest.tabs) {
    const metadata = TAB_METADATA[tab.slug];
    lines.push(`## ${metadata?.sectionTitle ?? tab.title}`);
    lines.push("");
    lines.push("<CardGroup cols={3}>");
    for (const service of tab.services) {
      lines.push(`  <Card title=\"${escapeMdx(trimApiDisplayTitle(service.title))}\" href=\"${escapeMdx(getPathname(service.fernDocsUrl))}\">`);
      lines.push(`    ${compactDescription(service.description)}`);
      lines.push("  </Card>");
    }
    lines.push("</CardGroup>");
    lines.push("");
  }

  return `${lines.join("\n")}\n`;
}

async function cleanRemovedArtifacts(manifestServiceIds) {
  const apiEntries = await fs.readdir(APIS_DIR, { withFileTypes: true });
  await Promise.all(
    apiEntries
      .filter((entry) => entry.isDirectory() && !manifestServiceIds.has(entry.name))
      .map((entry) => fs.rm(path.join(APIS_DIR, entry.name), { recursive: true, force: true })),
  );

  const pageEntries = await fs.readdir(SERVICE_PAGES_DIR, { withFileTypes: true });
  await Promise.all(
    pageEntries
      .filter((entry) => entry.isFile() && entry.name.endsWith(".mdx"))
      .filter((entry) => !manifestServiceIds.has(entry.name.replace(/\.mdx$/, "")))
      .map((entry) => fs.rm(path.join(SERVICE_PAGES_DIR, entry.name), { force: true })),
  );
}

async function main() {
  const [manifest, registry] = await Promise.all([loadManifest(), readJson(REGISTRY_PATH)]);
  const registryById = new Map(registry.map((service) => [service.id, service]));

  await fs.mkdir(APIS_DIR, { recursive: true });
  await fs.mkdir(SERVICE_PAGES_DIR, { recursive: true });

  const enrichedTabs = manifest.tabs.map((tab) => ({
    ...tab,
    services: tab.services.map((service) => ({
      ...service,
      description: registryById.get(service.id)?.description ?? "",
    })),
  }));

  const enrichedManifest = { ...manifest, tabs: enrichedTabs };
  const serviceIds = new Set(enrichedTabs.flatMap((tab) => tab.services.map((service) => service.id)));

  await cleanRemovedArtifacts(serviceIds);

  await fs.writeFile(DOCS_PATH, buildDocsYaml(enrichedManifest), "utf8");
  await fs.writeFile(LANDING_PATH, buildLandingPage(enrichedManifest), "utf8");

  for (const tab of enrichedTabs) {
    for (const service of tab.services) {
      const apiDir = path.join(APIS_DIR, service.id);
      await fs.mkdir(apiDir, { recursive: true });
      await fs.writeFile(path.join(apiDir, "generators.yml"), buildGeneratorYaml(service), "utf8");
      await fs.writeFile(path.join(SERVICE_PAGES_DIR, `${service.id}.mdx`), buildServicePage(service, tab), "utf8");
    }
  }

  console.log(`Generated Fern artifacts for ${enrichedManifest.serviceCount} service(s)`);
  console.log(`Manifest tabs: ${enrichedManifest.tabs.map((tab) => tab.slug).join(", ")}`);
  console.log(`Updated ${DOCS_PATH}`);
  console.log(`Updated ${LANDING_PATH}`);
}

main().catch((error) => {
  console.error("Failed to generate unified Fern artifacts:");
  console.error(error);
  process.exit(1);
});
