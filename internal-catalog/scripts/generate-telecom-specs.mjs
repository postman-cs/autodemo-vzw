const OP_METHOD = { list: "get", create: "post", get: "get", update: "put", delete: "delete", action: "post", search: "post" };

function buildResponses(op, resource) {
  const code = op === "create" ? "201" : "200";
  const desc = op === "create" ? "Resource created successfully" : op === "delete" ? "Resource deleted successfully" : "Successful response";
  return { [code]: { description: desc, content: { "application/json": { schema: { type: "object", properties: { id: { type: "string", description: `${resource} identifier`, example: `${resource}-a1b2c3d4` } } } } } } };
}

function buildRequestBody(op, resource) {
  if (!["create", "update", "action", "search"].includes(op)) return undefined;
  return {
    required: true,
    description: `${op === "search" ? "Search" : op === "action" ? "Action" : op.charAt(0).toUpperCase() + op.slice(1)} ${resource} request payload`,
    content: { "application/json": { schema: { type: "object", properties: { data: { type: "object", description: `${resource} data` } } } } },
  };
}

function buildParams(path) {
  const params = [];
  const matches = path.matchAll(/\{(\w+)\}/g);
  for (const m of matches) {
    params.push({ name: m[1], in: "path", required: true, description: `${m[1].replace(/_/g, " ")}`, schema: { type: "string" } });
  }
  return params;
}

function buildPagination() {
  return [
    { name: "page", in: "query", required: false, description: "Page number for pagination", schema: { type: "integer", default: 1, minimum: 1 } },
    { name: "per_page", in: "query", required: false, description: "Number of results per page (max 100)", schema: { type: "integer", default: 20, minimum: 1, maximum: 100 } },
    { name: "sort_by", in: "query", required: false, description: "Field to sort results by", schema: { type: "string" } },
    { name: "sort_order", in: "query", required: false, description: "Sort direction", schema: { type: "string", enum: ["asc", "desc"], default: "desc" } },
  ];
}

function buildSpec(spec) {
  const pathsObj = {};
  for (const [pathStr, config] of Object.entries(spec.paths)) {
    const pathItem = {};
    const pathParams = buildParams(pathStr);

    for (const op of config.ops) {
      const method = OP_METHOD[op];
      const opId = `${op}${config.resource.split("_").map(w => w.charAt(0).toUpperCase() + w.slice(1)).join("")}`;
      const opObj = {
        operationId: pathStr.includes("{") ? `${opId}ById` : opId,
        summary: `${op.charAt(0).toUpperCase() + op.slice(1)} ${config.resource.replace(/_/g, " ")}`,
        description: `${op.charAt(0).toUpperCase() + op.slice(1)} operation for ${config.resource.replace(/_/g, " ")} resources.`,
        tags: [spec.tags[0].name],
      };

      const params = [...pathParams];
      if (["list", "search"].includes(op)) params.push(...buildPagination());
      if (params.length > 0) opObj.parameters = params;

      const body = buildRequestBody(op, config.resource);
      if (body) opObj.requestBody = body;
      opObj.responses = buildResponses(op, config.resource);

      pathItem[method] = opObj;
    }
    pathsObj[pathStr] = pathItem;
  }

  return {
    openapi: "3.0.3",
    info: {
      title: spec.title,
      description: spec.desc,
      version: spec.version,
      contact: { name: CONTACT_NAME, email: CONTACT_EMAIL, url: CONTACT_URL },
      license: { name: "Proprietary", url: "https://acmetelecom.com/terms" },
      "x-acme-catalog-id": spec.id,
      "x-acme-domain": spec.domain,
    },
    servers: [
      { url: `https://${spec.id}.api.acmetelecom.com/v1`, description: "Production" },
      { url: `https://${spec.id}.staging.acmetelecom.com/v1`, description: "Staging" },
    ],
    security: [{ BearerAuth: [] }],
    tags: spec.tags.map(t => ({ name: t.name, description: t.desc })),
    paths: pathsObj,
    components: {
      securitySchemes: {
        BearerAuth: { type: "http", scheme: "bearer", bearerFormat: "JWT" },
        OAuth2: { type: "oauth2", flows: { authorizationCode: { authorizationUrl: "https://auth.acmetelecom.com/authorize", tokenUrl: "https://auth.acmetelecom.com/token", scopes: { "telecom:read": "Read telecom data", "telecom:write": "Write telecom data", "telecom:admin": "Administer telecom resources" } } } },
      },
      schemas: {
        Error: { type: "object", properties: { code: { type: "string" }, message: { type: "string" }, details: { type: "array", items: { type: "object" } } }, required: ["code", "message"] },
        PaginationMeta: { type: "object", properties: { page: { type: "integer" }, per_page: { type: "integer" }, total: { type: "integer" }, total_pages: { type: "integer" } } },
      },
    },
  };
}

